<h1 class="title">A Periodic Table ordered by Subshells</h1>


<p>Since studying chemistry at school I struggled to understand how the periodic table worked and why the elements were positioned where they were.  As a graduate studying materials science I still struggled to understand how the periodic table related to the structure of the elements.</p>  

<p>Textbooks show elements are ordered neatly by the energy level of their subshells.  Every physics, chemistry and material science textbook shows that the order of the subshells is 1s, 2s, 1p, 3s, 3p, 4s, 3d, 4p, 5s, 6s, 4f, 5d, 6p, 7s, 5f, 6d.  So why not in the periodic table?  The answer is the periodic table design was based on which elements had similar properties long before anyone knew what the subshells were.</p>

<p>Many students and teachers of chemistry are happy with the classic periodic table.  They understand the chemistry, the physics, and where things are on the periodic table, and it is a neat design, so why change it?  But for people like myself trying to learn chemistry, it may be easier to understand if everything is consistent: the energy level diagrams, the order of the subshells and the periodic table.</p>  


<p>In October 2009 an article was written outlining how a periodic table ordered by subshells might be useful and it was kindly published in the 
<a href="http://www.amsi.ge/jbpc/">Journal of Biological Physics and Chemistry</a>
in June 2010.  The advantages highlighted include the ability to visualise the table as an atom with layers of shells, the visual cohesiveness obtained by including the Actinide and Lanthanide groups in order in the main body of the table and the ability to see instantly how many electrons there are in the outer subshell of an element.     <br>

</p> 
    

