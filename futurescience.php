<h3>4. Conclusion</h3>
<p> A very small number of sources were used, and so important applications will have been missed.  Classification of major categories can be done in many ways, someone else may have categories for telecoms, mining, pharmaceuticals, automotive, aerospace and defence for instance.  Nanotechnology covers many areas that could be categorised separately.  It is likely there will be many alternative technologies competing in each of these areas, and it is impossible to predict the tools, materials and techniques that will be used.  Analysis of previous innovations suggests that most new technologies will be developed by teams of Phds in industry.  Analysis of Phds available in the UK, suggests that the UK is a leader in genetic engineering, but that there is much less research in the other areas.  </p>

